package org.lanqiao.eneity;

public class Account {
      private int id;
      private String name;
      private double bancle;
      
      
      
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getBancle() {
		return bancle;
	}
	public void setBancle(double bancle) {
		this.bancle = bancle;
	}
      
}
