package org.imooc.entliy;

public class User {
	private String sessionIdString;
	private String ipString;
	private String firstimeString;

	public String getSessionIdString() {
		return sessionIdString;
	}

	public void setSessionIdString(String sessionIdString) {
		this.sessionIdString = sessionIdString;
	}

	public String getIpString() {
		return ipString;
	}

	public void setIpString(String ipString) {
		this.ipString = ipString;
	}

	public String getFirstimeString() {
		return firstimeString;
	}

	public void setFirstimeString(String firstimeString) {
		this.firstimeString = firstimeString;
	}

}
